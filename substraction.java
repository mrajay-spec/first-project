public class substraction {
    public static void main(String[] args) {
        int a = 15;
        int b = 5;

        System.out.println("Before subtraction: a = " + a + ", b = " + b);

        // Subtracting b from a
        int result = a - b;

        System.out.println("After subtraction: result = " + result);
    }
}
